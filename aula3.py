"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Cauan Magno')
print(1,'Cauan "Magno"', sep='-')

# Aspas duplas
print("Cauan Magno")
print(2, "Cauan 'Magno'", sep='-')


# Escape -> \
print("Cauan\"Magno\"")

#r
print(r"Cauan \"Magno\"")
